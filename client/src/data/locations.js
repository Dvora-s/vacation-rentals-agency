// רשימת שמות הערים — איחוד הערים עם הרחובות והערים הנוספות, ללא כפילויות וממוין.
export const CITY_NAMES = Array.from(
  new Set([...CITIES_DATA.map((c) => c.city), ...ADDITIONAL_CITIES]),
).sort((a, b) => a.localeCompare(b, 'he'));

// מחזיר את רשימת הרחובות עבור עיר נתונה (מערך ריק אם אין רחובות במאגר).
export function getStreetsForCity(city) {
  if (!city) return [];
  const entry = CITIES_DATA.find((c) => c.city === city);
  return entry ? entry.streets : [];
}

// בדיקה אם עיר קיימת במאגר.
export function isKnownCity(city) {
  return CITY_NAMES.includes(city);
}

// בדיקה אם לעיר יש רשימת רחובות מפורטת במאגר.
export function cityHasStreets(city) {
  return getStreetsForCity(city).length > 0;
}
