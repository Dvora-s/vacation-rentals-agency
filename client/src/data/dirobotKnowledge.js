const NORMALIZE = (text) =>
  String(text || '').replace(/["'`׳״]/g, '"').replace(/\s+/g, ' ').trim().toLowerCase();

// מחזיר את התשובה המתאימה ביותר מהמאגר, או null אם אין התאמה.
export function knowledgeAnswer(text) {
  const t = NORMALIZE(text);
  if (!t) return null;

  let best = null;
  let bestScore = 0;
  for (const topic of KNOWLEDGE) {
    let score = 0;
    for (const kw of topic.keywords) {
      if (t.includes(kw.toLowerCase())) {
        // מילת מפתח ארוכה/ספציפית מקבלת משקל גבוה יותר.
        score += kw.includes(' ') ? 2 : 1;
      }
    }
    if (score > bestScore) {
      bestScore = score;
      best = topic;
    }
  }

  if (!best || bestScore === 0) return null;
  return { text: best.answer, actions: best.actions, topicId: best.id };
}
